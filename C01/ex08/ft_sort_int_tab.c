#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	swaps;
	int	tmp;

	swaps = 0;
	while (swaps < size)
	{
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = tmp;
			}
			i++;
		}
		swaps++;
	}
}
